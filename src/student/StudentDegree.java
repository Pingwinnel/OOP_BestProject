package student;
/**
 * The `StudentDegree` enumeration represents the academic degree levels of a student.
 * It includes BACHELOR, MASTER, and PHD as possible values.
 */
public enum StudentDegree {
    /**
     * Represents the Bachelors's degree level.
     */
	BACHELOR, 
	/**
     * Represents the Master's degree level.
     */
	MASTER, 
    /**
     * Represents the Doctor of Philosophy (Ph.D.) degree level.
     */
	PHD;
}
